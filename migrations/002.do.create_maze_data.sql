DROP TABLE IF EXISTS maze_data;
CREATE TABLE maze_data (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  hero INTEGER[] NOT NULL,
  token1 INTEGER[] NOT NULL,
  token2 INTEGER[] NOT NULL,
  token3 INTEGER[] NOT NULL,
  token4 INTEGER[] NOT NULL,
  walls INTEGER[][] NOT NULL,
  c INTEGER NOT NULL,
  r INTEGER NOT NULL,
  date_created TIMESTAMP DEFAULT now() NOT NULL,
  date_modified TIMESTAMP
);

